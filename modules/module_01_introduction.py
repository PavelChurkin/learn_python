# ============================================================================
# РАЗДЕЛ 1: Вывод данных с помощью функции print()
# ============================================================================

# Функция print() используется для вывода информации в консоль
print("Добро пожаловать в курс Python!")  # Выводим приветственное сообщение

# Можно выводить несколько значений через запятую
print("Python", "версии", 3)  # Выведет: Python версии 3

# Параметр sep определяет разделитель между значениями (по умолчанию пробел)
print("a", "b", "c", sep="-")  # Выведет: a-b-c

# Параметр end определяет, что будет в конце вывода (по умолчанию перенос строки)
print("Строка 1", end=" | ")  # Не переходит на новую строку
print("Строка 2")  # Выведет: Строка 1 | Строка 2

print()  # Пустая строка для разделения вывода

# ============================================================================
# РАЗДЕЛ 2: Переменные
# ============================================================================

# Переменная - это именованная область памяти для хранения данных
# В Python не нужно объявлять тип переменной явно (динамическая типизация)

# Создаём переменную с целым числом
age = 25  # Переменная age содержит целое число 25

# Создаём переменную со строкой
name = "Алексей"  # Переменная name содержит строку "Алексей"

# Переменные можно переназначать
age = 26  # Теперь age равно 26

# Множественное присваивание в одной строке
x, y, z = 1, 2, 3  # x=1, y=2, z=3

# Присваивание одного значения нескольким переменным
a = b = c = 0  # Все три переменные равны 0

# Вывод переменных
print("Имя:", name)  # Выводим значение переменной name
print("Возраст:", age)  # Выводим значение переменной age
print("Координаты:", x, y, z)  # Выводим несколько переменных

print()  # Пустая строка для разделения вывода

# ============================================================================
# РАЗДЕЛ 3: Правила именования переменных
# ============================================================================

# Правильные имена переменных:
user_name = "Иван"  # snake_case - рекомендуемый стиль для Python
userName = "Мария"  # camelCase - допустимо, но не рекомендуется в Python
_private = "скрытое"  # Начинается с подчёркивания (условно приватное)
MAX_SIZE = 100  # UPPER_CASE для констант

# Имена переменных чувствительны к регистру
Name = "Пётр"  # Это отдельная переменная, отличная от 'name'
print("name:", name, "| Name:", Name)  # Выведет разные значения

print()  # Пустая строка для разделения вывода

# ============================================================================
# РАЗДЕЛ 4: Основные типы данных
# ============================================================================

# --- Целые числа (int) ---
integer_number = 42  # Целое число
negative_number = -10  # Отрицательное целое число
big_number = 1_000_000  # Для читаемости можно использовать подчёркивания

# Проверка типа с помощью функции type()
print("Тип integer_number:", type(integer_number))  # Выведет: <class 'int'>

# --- Числа с плавающей точкой (float) ---
pi = 3.14159  # Число с дробной частью
scientific = 2.5e-3  # Научная нотация: 2.5 * 10^(-3) = 0.0025

print("Число пи:", pi)  # Выводим значение переменной
print("Научная нотация:", scientific)  # Выводим число в научной нотации
print("Тип pi:", type(pi))  # Выведет: <class 'float'>

# --- Строки (str) ---
string_single = 'Одинарные кавычки'  # Строка в одинарных кавычках
string_double = "Двойные кавычки"  # Строка в двойных кавычках
string_multiline = """Многострочная
строка с переносами"""  # Многострочная строка

print("Строка:", string_single)  # Выводим строку
print("Тип строки:", type(string_single))  # Выведет: <class 'str'>

# Экранирование специальных символов
escaped = "Текст в \"кавычках\""  # Экранируем кавычки внутри строки
newline = "Первая строка\nВторая строка"  # \n - символ переноса строки
tab = "Столбец1\tСтолбец2"  # \t - символ табуляции

print(escaped)  # Выводим строку с кавычками
print(newline)  # Выводим строку с переносом
print(tab)  # Выводим строку с табуляцией

# --- Логический тип (bool) ---
is_active = True  # Логическое значение "истина"
is_deleted = False  # Логическое значение "ложь"

print("Активен:", is_active)  # Выведет: True
print("Тип bool:", type(is_active))  # Выведет: <class 'bool'>

# --- Специальный тип None ---
result = None  # None означает отсутствие значения

print("Результат:", result)  # Выведет: None
print("Тип None:", type(result))  # Выведет: <class 'NoneType'>

print()  # Пустая строка для разделения вывода

# ============================================================================
# РАЗДЕЛ 5: Преобразование типов
# ============================================================================

# Преобразование в целое число
string_num = "123"  # Строка с числом
converted_int = int(string_num)  # Преобразуем строку в целое число
print("Преобразование '123' в int:", converted_int, type(converted_int))

# Преобразование в число с плавающей точкой
converted_float = float("3.14")  # Преобразуем строку в float
print("Преобразование '3.14' в float:", converted_float)

# Преобразование в строку
number = 42  # Целое число
converted_str = str(number)  # Преобразуем число в строку
print("Преобразование 42 в str:", converted_str, type(converted_str))

# Преобразование в логический тип
# Всё, что не пустое или не ноль, считается True
print("bool(1):", bool(1))  # True - ненулевое число
print("bool(0):", bool(0))  # False - ноль
print("bool(''):", bool(""))  # False - пустая строка
print("bool('текст'):", bool("текст"))  # True - непустая строка

print()  # Пустая строка для разделения вывода

# ============================================================================
# РАЗДЕЛ 6: Форматирование строк
# ============================================================================

# Переменные для примеров
name = "Мария"  # Имя пользователя
age = 30  # Возраст пользователя
salary = 75000.50  # Зарплата пользователя

# Метод 1: f-строки (рекомендуется с Python 3.6+)
greeting = f"Привет, {name}! Тебе {age} лет."  # Подстановка переменных
print(greeting)  # Выводим отформатированную строку

# f-строки с выражениями
print(f"Через 5 лет тебе будет {age + 5} лет")  # Вычисления внутри f-строки

# Форматирование чисел в f-строках
print(f"Зарплата: {salary:.2f} руб.")  # Два знака после запятой
print(f"Зарплата: {salary:,.2f} руб.")  # С разделителями тысяч

# Метод 2: Метод format()
template = "Имя: {}, Возраст: {}"  # Шаблон с placeholder'ами
print(template.format(name, age))  # Подставляем значения

# С именованными аргументами
template_named = "Имя: {n}, Возраст: {a}"  # Именованные placeholder'ы
print(template_named.format(n=name, a=age))  # Подставляем по именам

# Метод 3: Оператор % (устаревший, но встречается в старом коде)
print("Имя: %s, Возраст: %d" % (name, age))  # %s - строка, %d - целое число

print()  # Пустая строка для разделения вывода

