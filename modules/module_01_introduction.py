# Функция id() возвращает уникальный идентификатор объекта в памяти
print("ID test_var:", id(test_var))  # Выведет уникальный числовой ID

# Функция isinstance() проверяет принадлежность к типу
print("Это список?", isinstance(test_var, list))  # Выведет: True
