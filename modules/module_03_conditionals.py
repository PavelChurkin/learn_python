# match-case - структурное сопоставление с образцом
# Появился в Python 3.10

# Простой пример с литералами
def get_day_type(day):
    """Определяет тип дня недели"""
    match day:  # Сопоставляем значение day
        case "понедельник" | "вторник" | "среда" | "четверг" | "пятница":
            return "рабочий день"  # Будний день
        case "суббота" | "воскресенье":
            return "выходной"  # Выходной день
        case _:  # _ - это "wildcard", соответствует любому значению
            return "неизвестный день"  # Значение по умолчанию

# Тестируем функцию
print(f"Понедельник: {get_day_type('понедельник')}")  # рабочий день
print(f"Суббота: {get_day_type('суббота')}")  # выходной
print(f"Праздник: {get_day_type('праздник')}")  # неизвестный день

print()  # Пустая строка

# match-case с проверкой структуры
def describe_point(point):
    """Описывает точку на плоскости или в пространстве"""
    match point:  # Сопоставляем структуру point
        case (0, 0):  # Точка в начале координат
            return "Начало координат"
        case (0, y):  # Точка на оси Y (x = 0)
            return f"На оси Y, y = {y}"
        case (x, 0):  # Точка на оси X (y = 0)
            return f"На оси X, x = {x}"
        case (x, y):  # Любая другая точка на плоскости
            return f"Точка ({x}, {y})"
        case (x, y, z):  # Точка в 3D пространстве
            return f"3D точка ({x}, {y}, {z})"
        case _:  # Всё остальное
            return "Неизвестный формат"

# Тестируем функцию
print(describe_point((0, 0)))  # Начало координат
print(describe_point((0, 5)))  # На оси Y
print(describe_point((3, 0)))  # На оси X
print(describe_point((3, 4)))  # Точка (3, 4)
print(describe_point((1, 2, 3)))  # 3D точка

print()  # Пустая строка

# match-case с условиями (guards)
def categorize_number(n):
    """Категоризирует число"""
    match n:  # Сопоставляем значение n
        case 0:  # Ноль
            return "ноль"
        case n if n < 0:  # Отрицательное (с условием guard)
            return "отрицательное"
        case n if n % 2 == 0:  # Положительное чётное
            return "положительное чётное"
        case _:  # Всё остальное (положительное нечётное)
            return "положительное нечётное"

# Тестируем функцию
print(f"0: {categorize_number(0)}")  # ноль
print(f"-5: {categorize_number(-5)}")  # отрицательное
print(f"4: {categorize_number(4)}")  # положительное чётное
print(f"7: {categorize_number(7)}")  # положительное нечётное

