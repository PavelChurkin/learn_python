# Пример 1: Простой калькулятор
def calculator(a, b, operation):
    """Выполняет арифметическую операцию"""
    if operation == "+":  # Сложение
        return a + b
    elif operation == "-":  # Вычитание
        return a - b
    elif operation == "*":  # Умножение
        return a * b
    elif operation == "/":  # Деление
        if b != 0:  # Проверяем деление на ноль
            return a / b
        else:
            return "Ошибка: деление на ноль"
    else:  # Неизвестная операция
        return "Ошибка: неизвестная операция"

# Тестируем калькулятор
print(f"10 + 5 = {calculator(10, 5, '+')}")  # 15
print(f"10 - 5 = {calculator(10, 5, '-')}")  # 5
print(f"10 * 5 = {calculator(10, 5, '*')}")  # 50
print(f"10 / 5 = {calculator(10, 5, '/')}")  # 2.0
print(f"10 / 0 = {calculator(10, 0, '/')}")  # Ошибка

print()  # Пустая строка

# Пример 2: Определение високосного года
def is_leap_year(year):
    """Проверяет, является ли год високосным"""
    # Год високосный, если:
    # - делится на 4 И не делится на 100
    # - ИЛИ делится на 400
    if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
        return True  # Високосный
    else:
        return False  # Не високосный

# Тестируем функцию
years = [2000, 2020, 2021, 2024, 1900, 2100]  # Годы для проверки
for year in years:  # Перебираем годы
    status = "високосный" if is_leap_year(year) else "обычный"
    print(f"{year}: {status}")

