# Пример 3: Проверка возраста для разных категорий билетов
def get_ticket_price(age):
    """Возвращает цену билета в зависимости от возраста"""
    if age < 0:  # Некорректный возраст
        return "Ошибка: некорректный возраст"
    elif age < 3:  # Дети до 3 лет
        return "Бесплатно (дети до 3 лет)"
    elif age < 12:  # Дети 3-11 лет
        return "50% скидка (детский билет)"
    elif age < 18:  # Подростки 12-17 лет
        return "25% скидка (подростковый билет)"
    elif age < 65:  # Взрослые 18-64 лет
        return "Полная цена (взрослый билет)"
    else:  # Пенсионеры 65+ лет
        return "30% скидка (пенсионный билет)"

# Тестируем функцию
ages = [2, 8, 15, 30, 70]  # Возрасты для проверки
for age in ages:  # Перебираем возрасты
    print(f"Возраст {age}: {get_ticket_price(age)}")

