# Пример 3: с условным выражением (if-else)
# Синтаксис: [выражение1 if условие else выражение2 for элемент in последовательность]

# Замена отрицательных чисел нулями
numbers = [-3, 1, -7, 4, -2, 8]  # Список с отрицательными числами
