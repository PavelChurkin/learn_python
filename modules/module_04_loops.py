# Цикл while с условием выхода
# Поиск первого кратного 7 числа, большего 50

number = 51  # Начинаем с 51
while number % 7 != 0:  # Пока число не делится на 7 без остатка
    number += 1  # Переходим к следующему числу

print(f"Первое кратное 7 число после 50: {number}")  # 56

print()  # Пустая строка

# Эмуляция do-while (выполнить хотя бы один раз)
# В Python нет do-while, но можно эмулировать

attempts = 0  # Счётчик попыток
max_attempts = 3  # Максимальное число попыток

while True:  # Бесконечный цикл
    attempts += 1  # Увеличиваем счётчик
    print(f"  Попытка {attempts}")  # Выводим номер попытки

    # Проверяем условие выхода в конце
    if attempts >= max_attempts:  # Если достигли максимума
        break  # Выходим из цикла

print(f"Всего попыток: {attempts}")  # 3

print()  # Пустая строка

