# pass - заглушка, ничего не делает
print("Демонстрация pass:")
for i in range(5):  # Цикл от 0 до 4
    if i == 2:  # Если i равно 2
        pass  # Ничего не делаем (заглушка)
        # pass часто используется для пустых блоков во время разработки
    print(f"  i = {i}")  # Выводим значение (включая i = 2)

