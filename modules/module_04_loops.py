# Генератор списка - компактный способ создания списков

# Базовый синтаксис: [выражение for элемент in последовательность]

# Пример 1: квадраты чисел
# Обычный способ:
squares_loop = []  # Пустой список
for x in range(1, 6):  # Для каждого x от 1 до 5
    squares_loop.append(x ** 2)  # Добавляем квадрат x

# С генератором списка:
squares_comp = [x ** 2 for x in range(1, 6)]  # Одна строка!

print(f"Квадраты (цикл): {squares_loop}")  # [1, 4, 9, 16, 25]
print(f"Квадраты (генератор): {squares_comp}")  # [1, 4, 9, 16, 25]

print()  # Пустая строка

# Пример 2: с условием (фильтрация)
# Синтаксис: [выражение for элемент in последовательность if условие]

# Только чётные числа
evens = [x for x in range(1, 11) if x % 2 == 0]  # Фильтруем чётные
print(f"Чётные числа от 1 до 10: {evens}")  # [2, 4, 6, 8, 10]

# Квадраты нечётных чисел
odd_squares = [x ** 2 for x in range(1, 11) if x % 2 != 0]  # Фильтруем и преобразуем
