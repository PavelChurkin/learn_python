# Dictionary comprehension (генератор словаря)
# Синтаксис: {ключ: значение for элемент in последовательность}

words = ["apple", "banana", "cherry"]  # Список слов
word_lengths = {word: len(word) for word in words}  # Слово: длина
print(f"Длины слов: {word_lengths}")  # {'apple': 5, 'banana': 6, 'cherry': 6}

# Инвертирование словаря
original = {"a": 1, "b": 2, "c": 3}  # Исходный словарь
inverted = {v: k for k, v in original.items()}  # Меняем ключи и значения
print(f"Исходный: {original}")  # {'a': 1, 'b': 2, 'c': 3}
print(f"Инвертированный: {inverted}")  # {1: 'a', 2: 'b', 3: 'c'}

# Generator expression (генераторное выражение)
# Круглые скобки вместо квадратных - создаёт итератор, а не список
# Экономит память для больших последовательностей

sum_of_squares = sum(x ** 2 for x in range(1, 101))  # Сумма квадратов 1-100
