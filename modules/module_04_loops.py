# Пример 2: проверка на простое число
def is_prime(n):
    """Проверяет, является ли число простым"""
    if n < 2:  # Числа меньше 2 не являются простыми
        return False

    for i in range(2, int(n ** 0.5) + 1):  # Проверяем делители до корня n
        if n % i == 0:  # Если нашли делитель
            return False  # Число не простое
    else:  # Если делители не найдены
        return True  # Число простое

# Тестируем функцию
test_numbers = [2, 3, 4, 17, 18, 19, 20, 97]  # Числа для проверки
print("Проверка на простые числа:")
for num in test_numbers:
    result = "простое" if is_prime(num) else "составное"
    print(f"  {num}: {result}")

