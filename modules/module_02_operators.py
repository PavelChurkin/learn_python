# РАЗДЕЛ 9: Операторы идентичности (is, is not)
# ============================================================================

print("--- Операторы идентичности ---")
print()  # Пустая строка

# Оператор is проверяет, являются ли два объекта одним и тем же объектом
# (сравнивает id объектов, а не их значения)

a = [1, 2, 3]  # Создаём список
b = [1, 2, 3]  # Создаём другой список с такими же значениями
c = a  # c указывает на тот же объект, что и a

# Сравнение значений (==) vs сравнение идентичности (is)
print(f"a = {a}, id(a) = {id(a)}")  # Показываем ID объекта a
print(f"b = {b}, id(b) = {id(b)}")  # Показываем ID объекта b
print(f"c = {c}, id(c) = {id(c)}")  # Показываем ID объекта c

print(f"a == b: {a == b}")  # True - значения одинаковые
print(f"a is b: {a is b}")  # False - это разные объекты
print(f"a is c: {a is c}")  # True - это один и тот же объект

# Оператор is not проверяет, что объекты разные
print(f"a is not b: {a is not b}")  # True - это разные объекты

# Особый случай: сравнение с None
value = None  # Значение None
# Для сравнения с None ВСЕГДА используйте is, а не ==
print(f"value is None: {value is None}")  # True - правильный способ
print(f"value is not None: {value is not None}")  # False

# Кэширование малых целых чисел в Python (-5 до 256)
x = 100  # Малое целое число
y = 100  # Такое же малое целое число
print(f"100 is 100: {x is y}")  # True - Python кэширует малые числа

x = 1000  # Большое целое число
y = 1000  # Такое же большое целое число
# Результат может различаться в зависимости от реализации Python
print(f"1000 is 1000: {x is y}")  # Может быть True или False

print()  # Пустая строка

