# РАЗДЕЛ 4: Логические операторы
# ============================================================================

print("--- Логические операторы ---")
print()  # Пустая строка

a = True  # Первое логическое значение
b = False  # Второе логическое значение

# Логическое И (and) - True только если оба значения True
result = a and b  # True and False = False
print(f"{a} and {b}: {result}")  # False

# Логическое ИЛИ (or) - True если хотя бы одно значение True
result = a or b  # True or False = True
print(f"{a} or {b}: {result}")  # True

# Логическое НЕ (not) - инвертирует значение
result = not a  # not True = False
print(f"not {a}: {result}")  # False

# Комбинация логических операторов
x = 10  # Число для проверки
# Проверяем, находится ли x в диапазоне от 5 до 15
in_range = x >= 5 and x <= 15  # Должны выполниться оба условия
print(f"{x} в диапазоне [5, 15]: {in_range}")  # True

# Можно записать короче с цепочкой сравнений
in_range = 5 <= x <= 15  # Эквивалентная запись
print(f"{x} в диапазоне [5, 15] (короткая запись): {in_range}")  # True

# Практический пример: проверка допустимого возраста
age = 25  # Возраст пользователя
has_permission = True  # Есть ли разрешение
# Доступ разрешён если возраст >= 18 ИЛИ есть разрешение
can_access = age >= 18 or has_permission  # True
print(f"Доступ разрешён (возраст {age}, разрешение {has_permission}): {can_access}")

