# ============================================================================

print("--- Побитовые операторы ---")
print()  # Пустая строка

# Побитовые операторы работают с числами на уровне отдельных битов
a = 5  # В двоичной системе: 0101
b = 3  # В двоичной системе: 0011

print(f"a = {a} (двоичное: {bin(a)})")  # Показываем двоичное представление
print(f"b = {b} (двоичное: {bin(b)})")  # Показываем двоичное представление

# Побитовое И (&) - 1 только если оба бита равны 1
result = a & b  # 0101 & 0011 = 0001 = 1
print(f"{a} & {b} = {result} (двоичное: {bin(result)})")  # 1

# Побитовое ИЛИ (|) - 1 если хотя бы один бит равен 1
result = a | b  # 0101 | 0011 = 0111 = 7
print(f"{a} | {b} = {result} (двоичное: {bin(result)})")  # 7

# Побитовое исключающее ИЛИ (^) - 1 если биты разные
result = a ^ b  # 0101 ^ 0011 = 0110 = 6
print(f"{a} ^ {b} = {result} (двоичное: {bin(result)})")  # 6

# Побитовое НЕ (~) - инвертирует все биты
result = ~a  # ~0101 = ...11111010 = -6 (в дополнительном коде)
print(f"~{a} = {result}")  # -6

# Сдвиг влево (<<) - умножение на 2^n
result = a << 2  # Сдвигаем биты a на 2 позиции влево (умножаем на 4)
print(f"{a} << 2 = {result} (двоичное: {bin(result)})")  # 20

# Сдвиг вправо (>>) - целочисленное деление на 2^n
result = a >> 1  # Сдвигаем биты a на 1 позицию вправо (делим на 2)
print(f"{a} >> 1 = {result} (двоичное: {bin(result)})")  # 2

# Практический пример: проверка чётности через побитовое И
number = 7  # Число для проверки
# Последний бит чётного числа = 0, нечётного = 1
is_odd = number & 1  # 0111 & 0001 = 1 (нечётное)
print(f"Число {number} нечётное? {bool(is_odd)}")  # True

