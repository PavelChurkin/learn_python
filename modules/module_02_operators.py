# Логические операторы возвращают одно из значений, а не обязательно True/False
# and возвращает первое ложное значение или последнее значение
print("0 and 5:", 0 and 5)  # 0 (первое ложное)
print("3 and 5:", 3 and 5)  # 5 (последнее, т.к. оба истинные)

# or возвращает первое истинное значение или последнее значение
print("0 or 5:", 0 or 5)  # 5 (первое истинное)
print("3 or 5:", 3 or 5)  # 3 (первое истинное)
print("0 or '' or None:", 0 or "" or None)  # None (последнее, все ложные)

# Практическое применение: значение по умолчанию
name = ""  # Пустое имя (ложное значение)
display_name = name or "Гость"  # Если name пустое, используем "Гость"
print(f"Имя для отображения: {display_name}")  # Гость

# Практическое применение: короткое замыкание (short-circuit)
# Если первое условие False, второе не проверяется в and
# Если первое условие True, второе не проверяется в or

def check():  # Определяем функцию для демонстрации
    """Функция, которая печатает сообщение и возвращает True"""
    print("Функция check() была вызвана")  # Это сообщение появится только если функция выполнится
    return True  # Возвращаем True

print("Демонстрация короткого замыкания:")
result = False and check()  # check() НЕ будет вызвана, т.к. False and ... = False
print(f"False and check(): {result}")  # False

result = True or check()  # check() НЕ будет вызвана, т.к. True or ... = True
print(f"True or check(): {result}")  # True

print()  # Пустая строка

