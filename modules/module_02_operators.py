# РАЗДЕЛ 10: Приоритет операторов
# ============================================================================

print("--- Приоритет операторов ---")
print()  # Пустая строка

# Операторы выполняются в определённом порядке (от высшего к низшему):
# 1. () - скобки
# 2. ** - возведение в степень
# 3. +x, -x, ~x - унарные операторы
# 4. *, /, //, % - умножение, деление
# 5. +, - - сложение, вычитание
# 6. <<, >> - побитовые сдвиги
# 7. & - побитовое И
# 8. ^ - побитовое исключающее ИЛИ
# 9. | - побитовое ИЛИ
# 10. ==, !=, <, <=, >, >=, is, is not, in, not in - сравнения
# 11. not - логическое НЕ
# 12. and - логическое И
# 13. or - логическое ИЛИ

# Примеры приоритета
result = 2 + 3 * 4  # Сначала умножение, потом сложение
print(f"2 + 3 * 4 = {result}")  # 14, не 20

result = (2 + 3) * 4  # Скобки меняют порядок
print(f"(2 + 3) * 4 = {result}")  # 20

result = 2 ** 3 ** 2  # Возведение в степень выполняется справа налево
print(f"2 ** 3 ** 2 = {result}")  # 2 ** 9 = 512, не 64

result = -2 ** 2  # Степень имеет приоритет над унарным минусом
print(f"-2 ** 2 = {result}")  # -4, не 4

result = (-2) ** 2  # Скобки меняют порядок
print(f"(-2) ** 2 = {result}")  # 4

# Сложный пример
result = 10 + 5 * 2 - 3 ** 2  # 10 + 10 - 9 = 11
print(f"10 + 5 * 2 - 3 ** 2 = {result}")  # 11

# Рекомендация: используйте скобки для ясности, даже если они не обязательны
result = (10 + (5 * 2)) - (3 ** 2)  # Тот же результат, но понятнее
print(f"(10 + (5 * 2)) - (3 ** 2) = {result}")  # 11

print()  # Пустая строка

