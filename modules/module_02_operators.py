print("--- Операторы сравнения ---")
print()  # Пустая строка

x = 10  # Первое значение для сравнения
y = 5  # Второе значение для сравнения

# Равно (==)
result = x == y  # Проверяем равенство
print(f"{x} == {y}: {result}")  # False, т.к. 10 не равно 5

# Не равно (!=)
result = x != y  # Проверяем неравенство
print(f"{x} != {y}: {result}")  # True, т.к. 10 не равно 5

# Больше (>)
result = x > y  # Проверяем, больше ли x чем y
print(f"{x} > {y}: {result}")  # True, т.к. 10 > 5

# Меньше (<)
result = x < y  # Проверяем, меньше ли x чем y
print(f"{x} < {y}: {result}")  # False, т.к. 10 не меньше 5

# Больше или равно (>=)
result = x >= y  # Проверяем, больше или равно ли x значению y
print(f"{x} >= {y}: {result}")  # True

# Меньше или равно (<=)
result = x <= y  # Проверяем, меньше или равно ли x значению y
print(f"{x} <= {y}: {result}")  # False

# Цепочка сравнений (уникальная особенность Python)
z = 7  # Третье значение
result = y < z < x  # Проверяем: 5 < 7 < 10
print(f"{y} < {z} < {x}: {result}")  # True

# Сравнение строк (лексикографическое)
str1 = "apple"  # Первая строка
str2 = "banana"  # Вторая строка
print(f"'{str1}' < '{str2}': {str1 < str2}")  # True, т.к. 'a' < 'b'

