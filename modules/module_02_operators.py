print("--- Арифметические операторы ---")
print()  # Пустая строка

# Сложение (+)
a = 10  # Первое число
b = 3  # Второе число
result = a + b  # Складываем a и b
print(f"{a} + {b} = {result}")  # Выводим результат: 10 + 3 = 13

# Вычитание (-)
result = a - b  # Вычитаем b из a
print(f"{a} - {b} = {result}")  # Выводим результат: 10 - 3 = 7

# Умножение (*)
result = a * b  # Умножаем a на b
print(f"{a} * {b} = {result}")  # Выводим результат: 10 * 3 = 30

# Деление (/) - всегда возвращает float
result = a / b  # Делим a на b
print(f"{a} / {b} = {result}")  # Выводим результат: 10 / 3 = 3.333...

# Целочисленное деление (//) - отбрасывает дробную часть
result = a // b  # Целочисленное деление a на b
print(f"{a} // {b} = {result}")  # Выводим результат: 10 // 3 = 3

# Остаток от деления (%)
result = a % b  # Находим остаток от деления a на b
print(f"{a} % {b} = {result}")  # Выводим результат: 10 % 3 = 1

# Возведение в степень (**)
result = a ** b  # Возводим a в степень b
print(f"{a} ** {b} = {result}")  # Выводим результат: 10 ** 3 = 1000

# Унарный минус (-)
c = -a  # Меняем знак числа
print(f"-{a} = {c}")  # Выводим результат: -10

# Унарный плюс (+) - не меняет значение, но может преобразовать к числу
d = +a  # Унарный плюс (редко используется)
print(f"+{a} = {d}")  # Выводим результат: 10

print()  # Пустая строка

