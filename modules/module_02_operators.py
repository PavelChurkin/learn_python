# РАЗДЕЛ 8: Операторы принадлежности (in, not in)
# ============================================================================

print("--- Операторы принадлежности ---")
print()  # Пустая строка

# Оператор in проверяет наличие элемента в последовательности
fruits = ["яблоко", "банан", "вишня"]  # Список фруктов

# Проверка наличия в списке
result = "банан" in fruits  # Есть ли "банан" в списке
print(f"'банан' in {fruits}: {result}")  # True

result = "груша" in fruits  # Есть ли "груша" в списке
print(f"'груша' in {fruits}: {result}")  # False

# Оператор not in проверяет отсутствие элемента
result = "груша" not in fruits  # "груша" отсутствует в списке
print(f"'груша' not in {fruits}: {result}")  # True

# Проверка подстроки в строке
text = "Привет, мир!"  # Строка для проверки
result = "мир" in text  # Есть ли подстрока "мир" в строке
print(f"'мир' in '{text}': {result}")  # True

# Проверка ключа в словаре
person = {"name": "Иван", "age": 30}  # Словарь
result = "name" in person  # Проверяем наличие ключа
print(f"'name' in {person}: {result}")  # True
result = "city" in person  # Проверяем отсутствующий ключ
print(f"'city' in {person}: {result}")  # False

print()  # Пустая строка

