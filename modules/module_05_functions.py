# Декоратор - функция, которая модифицирует поведение другой функции

import time  # Для измерения времени

# Простой декоратор
def log_call(func):
    """Декоратор, логирующий вызов функции"""
    def wrapper(*args, **kwargs):
        """Обёртка, которая логирует и вызывает оригинальную функцию"""
        print(f"Вызов функции {func.__name__} с аргументами {args}, {kwargs}")
        result = func(*args, **kwargs)  # Вызываем оригинальную функцию
        print(f"Функция {func.__name__} вернула {result}")
        return result
    return wrapper  # Возвращаем обёртку

# Применение декоратора
@log_call  # Эквивалентно: add = log_call(add)
def add(a, b):
    """Складывает два числа"""
    return a + b

result = add(3, 5)  # Вызываем декорированную функцию

print()  # Пустая строка

# Декоратор для измерения времени выполнения
def timer(func):
    """Декоратор, измеряющий время выполнения функции"""
    def wrapper(*args, **kwargs):
        start = time.time()  # Запоминаем время начала
        result = func(*args, **kwargs)  # Выполняем функцию
        end = time.time()  # Запоминаем время окончания
        print(f"Функция {func.__name__} выполнилась за {end - start:.6f} секунд")
        return result
    return wrapper

@timer
def slow_function():
    """Медленная функция для демонстрации"""
    total = 0
    for i in range(100000):  # Длительные вычисления
        total += i
    return total

result = slow_function()

print()  # Пустая строка

# Декоратор с параметрами
def repeat(times):
    """Декоратор, повторяющий вызов функции n раз"""
    def decorator(func):
        def wrapper(*args, **kwargs):
            for _ in range(times):  # Повторяем times раз
                result = func(*args, **kwargs)
            return result
        return wrapper
    return decorator

@repeat(3)  # Вызывать функцию 3 раза
def say_hello():
    """Выводит приветствие"""
    print("Привет!")

say_hello()  # Выведет "Привет!" три раза

