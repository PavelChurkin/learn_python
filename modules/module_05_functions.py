# Пример 1: Валидация данных
def validate_email(email):
    """
    Простая проверка формата email.

    Args:
        email: Строка для проверки

    Returns:
        bool: True если формат корректный
    """
    if "@" not in email:  # Должна быть @
        return False
    if "." not in email.split("@")[1]:  # Должна быть точка после @
        return False
    if email.count("@") != 1:  # Должна быть только одна @
        return False
    return True

