# Оптимизированная версия с мемоизацией
def fibonacci_memo(n, memo=None):
    """Вычисляет n-е число Фибоначчи с мемоизацией"""
    if memo is None:  # Инициализируем кэш при первом вызове
        memo = {}

    if n in memo:  # Если результат уже вычислен
        return memo[n]

    if n <= 1:  # Базовые случаи
        result = n
    else:  # Рекурсивный случай
        result = fibonacci_memo(n - 1, memo) + fibonacci_memo(n - 2, memo)

    memo[n] = result  # Сохраняем результат в кэш
    return result

print(f"fibonacci_memo(30) = {fibonacci_memo(30)}")  # Быстро вычисляется

