# Рекурсия - функция, вызывающая сама себя

# Факториал: n! = n * (n-1) * ... * 1
def factorial(n):
    """Вычисляет факториал числа рекурсивно"""
    if n <= 1:  # Базовый случай
        return 1
    else:  # Рекурсивный случай
        return n * factorial(n - 1)  # Вызываем себя с меньшим аргументом

print(f"factorial(5) = {factorial(5)}")  # 120
print(f"factorial(10) = {factorial(10)}")  # 3628800

