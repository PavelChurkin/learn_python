# Вложенные функции и nonlocal
def outer():
    """Внешняя функция"""
    x = "внешняя"  # Переменная внешней функции

    def inner():
        """Внутренняя функция"""
        nonlocal x  # Указываем, что используем переменную из внешней функции
        x = "изменена внутренней"  # Изменяем переменную внешней функции
        print(f"Внутри inner: x = {x}")

    print(f"До вызова inner: x = {x}")
    inner()  # Вызываем внутреннюю функцию
    print(f"После вызова inner: x = {x}")

