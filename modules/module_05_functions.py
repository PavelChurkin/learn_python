def calculate_area(width, height, unit="м"):
    """
    Вычисляет площадь прямоугольника.

    Args:
        width (float): Ширина прямоугольника
        height (float): Высота прямоугольника
        unit (str): Единица измерения (по умолчанию "м")

    Returns:
        str: Строка с результатом в формате "Площадь: X кв.ед"

    Raises:
        ValueError: Если width или height отрицательны

    Examples:
        >>> calculate_area(5, 3)
        'Площадь: 15 кв.м'
        >>> calculate_area(2, 4, "см")
        'Площадь: 8 кв.см'
    """
    if width < 0 or height < 0:  # Проверка входных данных
        raise ValueError("Ширина и высота должны быть неотрицательными")

    area = width * height  # Вычисляем площадь
    return f"Площадь: {area} кв.{unit}"  # Возвращаем результат

# Использование функции
