# Пример 2: Функция форматирования
def format_currency(amount, currency="RUB", decimals=2):
    """
    Форматирует число как денежную сумму.

    Args:
        amount: Сумма
        currency: Код валюты
        decimals: Количество знаков после запятой
    """
    symbols = {"RUB": "₽", "USD": "$", "EUR": "€"}
    symbol = symbols.get(currency, currency)
    return f"{amount:,.{decimals}f} {symbol}"

print(f"format_currency(1234567.89): {format_currency(1234567.89)}")
print(f"format_currency(1000, 'USD'): {format_currency(1000, 'USD')}")
print(f"format_currency(500.5, 'EUR', 0): {format_currency(500.5, 'EUR', 0)}")

