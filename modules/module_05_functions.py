# Числа Фибоначчи: F(n) = F(n-1) + F(n-2)
def fibonacci(n):
    """Вычисляет n-е число Фибоначчи рекурсивно"""
    if n <= 1:  # Базовые случаи: F(0) = 0, F(1) = 1
        return n
    else:  # Рекурсивный случай
        return fibonacci(n - 1) + fibonacci(n - 2)

print("Первые 10 чисел Фибоначчи:")
for i in range(10):
    print(fibonacci(i), end=" ")
print()

